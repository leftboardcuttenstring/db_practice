insert into table (firstColumn, secondColumn) values ('firstData', 'secondData')

/*
DISCRIPTION:

insert into table (firstColumn, secondColumn) values ('firstData', 'secondData'):
	insert into                            - that is keyword for inserting the data
	table                                  - you're mentioning to what you want to insert the data
	(firstColumn, secondColumn) 
		values ('firstData', 'secondData') - the relationship. The firstData value will be inserted 
											 into the firstColumn row, and the secondData value will 
											 be inserted into the secondColumn row. This always works. 
											 Just be sure to match the data types in the column and 
											 the data you want to write
*/